package com.escape.www.dto;

public class UserDTO {
	private String user_email;
	private String user_name;
	private String user_id;
	private String user_pw;
	private String user_prefer;
	private String user_phone;
	private int user_trophy;
	private String user_jDate;
	
	public String getUser_email() {
		return user_email;
	}
	public void setUser_email(String user_email) {
		this.user_email = user_email;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getUser_pw() {
		return user_pw;
	}
	public void setUser_pw(String user_pw) {
		this.user_pw = user_pw;
	}
	public String getUser_prefer() {
		return user_prefer;
	}
	public void setUser_prefer(String user_prefer) {
		this.user_prefer = user_prefer;
	}
	public String getUser_phone() {
		return user_phone;
	}
	public void setUser_phone(String user_phone) {
		this.user_phone = user_phone;
	}
	public int getUser_trophy() {
		return user_trophy;
	}
	public void setUser_trophy(int user_trophy) {
		this.user_trophy = user_trophy;
	}
	public String getUser_jDate() {
		return user_jDate;
	}
	public void setUser_jDate(String user_jDate) {
		this.user_jDate = user_jDate;
	}
	
	
}
